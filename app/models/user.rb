class User < ApplicationRecord
  # :lockable, :timeoutable, and :omniauthable
  devise :database_authenticatable,
         :confirmable,
         :registerable,
         :recoverable,
         :rememberable,
         :trackable,
         :validatable

  has_one_attached :avatar

  validates :phone, format: { with: /\A\d{10}\z/, message: 'must be 10 digits' }, allow_blank: true
end
